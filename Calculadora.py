class Calculadora():
    # Se puede poner "pass" para indicar al compilador que esta correcto
    # porque aun no lo implementas

    def suma(self, num1, num2):
        # En consola el "." significa que la prueba paso, "F" significa que la
        # prueba fallo
        return num1 + num2

    def resta(self, num1, num2):
        return num1 - num2
